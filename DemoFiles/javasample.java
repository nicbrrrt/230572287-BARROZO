public class SumOfNumbers {
    public static void main(String[] args) {
        int sum = 0;

        // Loop through numbers 1 to 10
        for (int i = 1; i <= 10; i++) {
            sum += i;
        }

        // Print the result
        System.out.println("The sum of the first 10 natural numbers is: " + sum);
    }
}
